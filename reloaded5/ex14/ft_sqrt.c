int	ft_sqrt(int	nb)
{
	int	rc;
	int	i;

	i = 0;
	rc = 0;
	if (nb < 0)
		return (0);
	else
	{
		while ((i * i <= nb) && (i <= 46340))
		{
			if (i * i == nb)
				return (i);
			i++;
		}
		return (0);
	}
}
